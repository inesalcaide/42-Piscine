int	ft_str_is_uppercase(char *str)
{
	int	is_uppercase;
	int	i;

	is_uppercase = 1;
	i = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] < 65 || str[i] > 90)
		{
			is_uppercase = 0;
		}
		i++;
	}
	return (is_uppercase);
}
